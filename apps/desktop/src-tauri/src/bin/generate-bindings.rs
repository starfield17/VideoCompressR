#[path = "../contracts.rs"]
mod contracts;

use std::env;
use std::fs;
use std::path::PathBuf;
use ts_rs::TS;

fn exported(declaration: String) -> String {
    declaration.replacen("type ", "export type ", 1)
}

fn generated_text() -> String {
    let declarations = [
        contracts::SettingsDto::decl(),
        contracts::PlanRequestDto::decl(),
        contracts::PlanItemDto::decl(),
        contracts::PlanResponseDto::decl(),
        contracts::BootstrapDto::decl(),
        contracts::AppSettingsDto::decl(),
        contracts::ApiErrorDto::decl(),
        contracts::QueueProgressDto::decl(),
        contracts::QueueItemResultDto::decl(),
        contracts::QueueItemDto::decl(),
        contracts::QueueMetricsDto::decl(),
        contracts::QueueStateDto::decl(),
        contracts::QueueSnapshotDto::decl(),
        contracts::QueueProgressDeltaDto::decl(),
        contracts::ActivityEventDto::decl(),
        contracts::QueueStreamMessage::decl(),
        contracts::PreviewOptionsDto::decl(),
        contracts::PreviewResultDto::decl(),
    ];
    let mut text =
        String::from("/* @generated by src-tauri/src/bin/generate-bindings.rs; do not edit. */\n");
    for declaration in declarations {
        text.push_str(&exported(declaration));
        text.push('\n');
    }
    text.push_str(
        "export type Codec = SettingsDto['codec'];\nexport type EncoderBackend = SettingsDto['backend'];\nexport type DecodeAcceleration = SettingsDto['decodeAcceleration'];\nexport type ContainerFormat = SettingsDto['container'];\nexport type AudioMode = SettingsDto['audioMode'];\n",
    );
    text
}

fn output_path() -> Result<PathBuf, String> {
    let mut args = env::args().skip(1);
    let mut output = None;
    while let Some(argument) = args.next() {
        if argument != "--output" {
            return Err(format!("unknown argument: {argument}"));
        }
        let value = args.next().ok_or_else(|| "--output requires a path".to_owned())?;
        output = Some(PathBuf::from(value));
    }
    if let Some(path) = output {
        return Ok(path);
    }
    let manifest = PathBuf::from(
        env::var_os("CARGO_MANIFEST_DIR")
            .ok_or_else(|| "CARGO_MANIFEST_DIR is missing".to_owned())?,
    );
    let frontend_root =
        manifest.parent().ok_or_else(|| "Tauri manifest has no frontend parent".to_owned())?;
    Ok(frontend_root.join("src/api/generated.ts"))
}

fn main() {
    let output = match output_path() {
        Ok(value) => value,
        Err(error) => {
            eprintln!("codegen error: {error}");
            std::process::exit(2);
        }
    };
    if let Some(parent) = output.parent() {
        if let Err(error) = fs::create_dir_all(parent) {
            eprintln!("cannot create generated binding directory {}: {error}", parent.display());
            std::process::exit(1);
        }
    }
    if let Err(error) = fs::write(&output, generated_text()) {
        eprintln!("cannot write generated TypeScript bindings {}: {error}", output.display());
        std::process::exit(1);
    }
    println!("generated {}", output.display());
}
